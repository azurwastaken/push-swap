#include "pswap.h"

int		is_int(int a)
{
	return ((a >= '0' && a <= '9') || a == '-' || a == '+');
}

int		is_num(int a)
{
	return (a >= '0' && a <= '9');
}

int		is_space(char c)
{
	return (c == ' ' || c == '\t' || c == '\n' || c == '\v' || c == '\r');
}

int		is_valid_int(long nb)
{
	return (nb > 2147483647 || nb < -2147483648);
}
